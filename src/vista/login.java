    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        sqlusuarios modSql = new sqlusuarios();
        usuario mod = new usuario();
       

        String pass = new String(txtpassword.getPassword());

        if (!txtusuario.getText().equals("") && !pass.equals("")) {

            String nuevoPass = hash.sha1(pass);

            mod.setUserName(txtusuario.getText());
            mod.setPassword(nuevoPass);
            

            if (modSql.login(mod)) {
                
                frmlog = null; 
                this.dispose();
                JOptionPane.showMessageDialog(null, "Bienvenido al sistema");
                panprin frmpaprin = new panprin();
                frmpaprin.setVisible(true);
                
            } else {
                JOptionPane.showMessageDialog(null, "Datos incorrectos, usuario o  contrase침a incorrectos");
            }
        
        } else {
         JOptionPane.showMessageDialog(null, "Debe ingresar su usuario y contrase침a, si no esta registrado debe registrarse");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

